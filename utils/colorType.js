export default function getColorByType(type) {

    if (type == "grass") {
        return "#65ba5c"
    } else if (type == "fire") {
        return "#fe9c51"
    } else if (type == "water") {
        return "#4c8fd5"
    } else if (type == "bug") {
        return "#91be2c"
    } else if (type == "normal") {
        return "#8f99a2"
    } else if (type == "poison") {
        return "#ab69ca"
    } else if (type == "electric") {
        return "#f4d03a"
    } else if (type == "ground") {
        return "#d97746"
    } else if (type == "fairy") {
        return "#ed90e8"
    } else if (type == "fighting") {
        return "#ce3f6b"
    } else if (type == "psychic") {
        return "#f97175"
    } else if (type == "rock") {
        return "#c8b58a"
    } else if (type == "ghost") {
        return "#516aab"
    } else if (type == "ice") {
        return "#73cebe"
    } else if (type == "dragon") {
        return "#086cc2"
    } else if (type == "flying") {
        return "#93aade"
    } else if (type == "steel") {
        return "#598fa1"
    } else if (type == "dark") {
        return "#5b5265"
    }
}